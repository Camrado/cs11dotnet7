// unsigned integer means positive whole number or 0
uint naturalNumber = 23;

// integer means negative or positive whole number or 0
int integerNumber = -23;

// float means single-precision floating point. It is a 32-bit type that can represent numbers with a precision of approximately 7 digits.
// F (or f) suffix makes it a float literal
float realNumber = 2.3f;

// double means double-precision floating point. It is a 64-bit type that can represent numbers with a precision of approximately 15-16 digits.
// double is the default type for a number value with a decimal point.
double anotherRealNumber = 2.3; // double literal